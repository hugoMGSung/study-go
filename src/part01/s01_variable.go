package main

import (
	"fmt"
)

func main1() {
	// 정수타입 : 0,1,-5, 실수 : 3.141592, 문자열 : "Hello", 불린 : true/false
	var i int
	var f float32 = 3.14
	var s string = "What!"
	var z = 4.56
	var g = "Hello~"
	var t = true

	fmt.Println("i = ", i)
	fmt.Println("f = ", f)
	fmt.Println("s = ", s)
	fmt.Println("z = ", z)
	fmt.Println("g = ", g)
	fmt.Println("t = ", t)
}
